
public class SVD {

	private Complex[][] u;
	private Complex[][] s;
	private Complex[][] v;

	public SVD() {
		super();
	}

	public Complex[][] getU() {
		return u;
	}

	public void setU(Complex[][] u) {
		this.u = u;
	}

	public Complex[][] getS() {
		return s;
	}

	public void setS(Complex[][] s) {
		this.s = s;
	}

	public Complex[][] getV() {
		return v;
	}

	public void setV(Complex[][] v) {
		this.v = v;
	}

}
